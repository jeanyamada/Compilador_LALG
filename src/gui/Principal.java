    public static void clearTableLexico(){        
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTableAnaliseLexica.getModel();
        
        while(defaultTableModel.getRowCount() > 0)
            defaultTableModel.removeRow(0);
    }
    
    public static void insereTableLexico(String lexema,String tokenName,String linha,String colunaInit,String colunaEnd,String erro){
        String [] table = {lexema,tokenName,linha,colunaInit,colunaEnd,erro};
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTableAnaliseLexica.getModel();
        defaultTableModel.addRow(table);
        
    }
    
    
    public static void clearTableSintatico(){        
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTableAnaliseSintatica.getModel();
        
        while(defaultTableModel.getRowCount() > 0)
            defaultTableModel.removeRow(0);
    }
    
    public static void insereTableSintatico(Stack stack,String entrada,String erro){
        String stringStack = "";
        
        if(stack != null)
            while(!stack.empty())
                stringStack += stack.pop().toString()+ " ";
        
        String [] table = {stringStack,entrada,erro};
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTableAnaliseSintatica.getModel();
        defaultTableModel.addRow(table);
        
    }
    
