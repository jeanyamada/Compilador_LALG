    private void initLineNumbers() {

        linesTextArea = new JTextArea("0");
        linesTextArea.setBackground(Color.LIGHT_GRAY);        
        linesTextArea.setEditable(false);
        linesTextArea.setFont(new Font("monospaced", Font.PLAIN, 12));        

        jTextArea1.getDocument().addDocumentListener(new DocumentListener(){
            
            public String getText(){
                int caretPosition = jTextArea1.getDocument().getLength();
                Element root = jTextArea1.getDocument().getDefaultRootElement();
                String text = "0" + System.getProperty("line.separator");
                
                for (int i = 1; i < root.getElementIndex( caretPosition ) + 1; i++){
                    text += i + System.getProperty("line.separator");
                }
                
                return text;
            }
            
            @Override
            public void changedUpdate(DocumentEvent de) {
                linesTextArea.setText(getText());
            }

            @Override
            public void insertUpdate(DocumentEvent de) {
                linesTextArea.setText(getText());
            }

            @Override
            public void removeUpdate(DocumentEvent de) {
                linesTextArea.setText(getText());
            } 
        });
        
        jTextArea1.setTabSize(3);
        jTextArea1.setFont(new Font("monospaced", Font.PLAIN, 12)); 
        jScrollPane1.getViewport().add(jTextArea1);
        jScrollPane1.setRowHeaderView(linesTextArea);
        jScrollPane1.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
    }
